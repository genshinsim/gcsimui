
export function ascLvlMin(asc) {
    switch (asc) {
        case 1:
            return 20;
        case 2:
            return 40;
        case 3:
            return 50;
        case 4:
            return 60;
        case 5:
            return 70;
        case 6:
            return 80;
    }
    return 1;
}

export function charAscLvlCap(asc) {
    switch (asc) {
        case 0:
            return 20;
        case 1:
            return 40;
        case 2:
            return 50;
        case 3:
            return 60;
        case 4:
            return 70;
        case 5:
            return 80;
        case 6:
            return 90;
    }
    return 0;
}